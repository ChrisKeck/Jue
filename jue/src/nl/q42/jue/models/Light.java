package nl.q42.jue.models;

/**
 * Basic light information
 */
public class Light {
	private String id;
	private String name;
	
	/**
	 * Return the ID of the light.
	 * @return id of the light
	 */
	public String getID() {
		return id;
	}
	
	/**
	 * Return the name of the light.
	 * @return name of the light
	 */
	public String getName() {
		return name;
	}
}
